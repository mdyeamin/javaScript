var vowel = "A".toLowerCase()
switch (vowel) {
    case "a":
        console.log("vowel a");
        break;
    case "e":
        console.log("vowel e");
        break;
    case "i":
        console.log("vowel i");
        break;
    case "o":
        console.log("vowel o");
        break;
    case "u":
        console.log("vowel u");
        break;
    default:
        console.log("invalid")
}